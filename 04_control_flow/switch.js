const month = 3

switch (month) {
    case 1:
        console.log("Jan")   
        break;
    case 2:
        console.log("Feb")   
        break;
    case 3:
        console.log("Mar")  
        break;
    case 4:
        console.log("Apr")   
        break;
    default: "Default case match"
        break;
}


const day = "tuesday"

switch (day) {
    case "monday":
        console.log("Mon")   
        break;
    case "tuesday":
        console.log("Tue")   
        break;
    case "thursday":
        console.log("Thu")   
        break;
    case "friday":
        console.log("Fri")   
        break;
    case "saturday":
        console.log("Sat")   
        break;
    case "sunday":
        console.log("Sun")   
        break;
    default:
        console.log("Default case match")
        break;
}